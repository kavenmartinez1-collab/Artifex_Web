if (ksigns.length !== 128) throw new Error(`ksigns length ${ksigns.length} != 128`);

// WGSL grid: array<u32, 512> — entry k → lo=words[2k], hi=words[2k+1].
const gwords = [];
for (const v of grid) {
  gwords.push(Number(v & 0xffffffffn));
  gwords.push(Number((v >> 32n) & 0xffffffffn));
}
// WGSL signs: array<u32, 32> — 4 packed bytes per word, little-endian.
const swords = [];
for (let k = 0; k < 32; k++) {
  swords.push((ksigns[4*k]) | (ksigns[4*k+1] << 8) | (ksigns[4*k+2] << 16) | (ksigns[4*k+3] << 24) >>> 0);
}

const hex = (x) => '0x' + (x >>> 0).toString(16).padStart(8, '0') + 'u';
const fmt = (arr, perLine) => {
  const lines = [];
  for (let i = 0; i < arr.length; i += perLine) {
    lines.push('  ' + arr.slice(i, i + perLine).map(hex).join(', ') + ',');
  }
  return lines.join('\n');
};

